function App() {
  const [provider, setProvider] = useState(null)
  const [signer, setSigner] = useState(null)
  const [contract, setContract] = useState(null)
  const [account, setAccount] = useState("")
  const [userTokens, setUserTokens] = useState([])
  const [loading, setLoading] = useState(true)
  const [activeTab, setActiveTab] = useState("mint")

  const contractAddress = "0x5219015234d02F59B21A501fb52062f8bDF74E1A";

  useEffect(() => {
    const init = async () => {
      if (window.ethereum) {
        try {
          // Request account access
          const accounts = await window.ethereum.request({ method: "eth_requestAccounts" })
          const provider = new ethers.providers.Web3Provider(window.ethereum)
          const signer = provider.getSigner()
          const contract = new ethers.Contract(contractAddress, contractABI, signer)

          setProvider(provider)
          setSigner(signer)
          setContract(contract)
          setAccount(accounts[0])
          setLoading(false)

          // Listen for account changes
          window.ethereum.on("accountsChanged", (accounts) => {
            setAccount(accounts[0])
            fetchUserTokens(accounts[0], contract)
          })
        } catch (error) {
          console.error("Error connecting to MetaMask", error)
          setLoading(false)
        }
      } else {
        console.log("Please install MetaMask!")
        setLoading(false)
      }
    }

    init()
  }, [])

  useEffect(() => {
    if (contract && account) {
      fetchUserTokens(account, contract)
    }
  }, [contract, account])

  const fetchUserTokens = async (address, contract) => {
    try {
      const balance = await contract.balanceOf(address)
      const tokenIds = []

      for (let i = 0; i < balance; i++) {
        const tokenId = await contract.tokenOfOwnerByIndex(address, i)
        tokenIds.push(tokenId.toString())
      }

      setUserTokens(tokenIds)
    } catch (error) {
      console.error("Error fetching user tokens:", error)
    }
  }

  const connectWallet = async () => {
    if (window.ethereum) {
      try {
        const accounts = await window.ethereum.request({ method: "eth_requestAccounts" })
        setAccount(accounts[0])
      } catch (error) {
        console.error("Error connecting to wallet:", error)
      }
    } else {
      alert("Please install MetaMask!")
    }
  }

  return (
    <div className="app">
      <Header account={account} connectWallet={connectWallet} activeTab={activeTab} setActiveTab={setActiveTab} />

      <main className="main-content">
        {loading ? (
          <div className="loading">
            <div className="loading-circle"></div>
            <p>Loading...</p>
          </div>
        ) : (
          <>
            {!account ? (
              <div className="connect-prompt">
                <h2>Welcome to Omotenashi Passport</h2>
                <p>Connect your wallet to start your journey through Japan's hidden gems</p>
                <button className="connect-button" onClick={connectWallet}>
                  Connect Wallet
                </button>
              </div>
            ) : (
              <div className="content-container">
                {activeTab === "mint" && (
                  <MintSection
                    contract={contract}
                    account={account}
                    fetchUserTokens={() => fetchUserTokens(account, contract)}
                  />
                )}

                {activeTab === "passport" && <PassportGallery contract={contract} userTokens={userTokens} />}

                {activeTab === "checkin" && (
                  <CheckInForm
                    contract={contract}
                    userTokens={userTokens}
                    fetchUserTokens={() => fetchUserTokens(account, contract)}
                  />
                )}

                {activeTab === "perks" && <RedeemPerks contract={contract} userTokens={userTokens} />}

                {activeTab === "locations" && <LocationsGuide />}
              </div>
            )}
          </>
        )}
      </main>

      <Footer />
    </div>
  )
}

export default App

